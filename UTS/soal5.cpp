#include <iostream>
using namespace std;

int main(){
    double a, b;

    cout << "Masukkan Angka Pertama : ";
    cin >> a;
    cout << "Masukkan Angka Kedua   : ";
    cin >> b;
    cout << "Hasil Penjmlahan: " << a + b << endl;
    cout << "Hasil Pengurangan: " << a - b << endl;
    cout << "Hasil Perkalian: " << a * b << endl;
    cout << "Hasil Pembagian: " << a / b << endl;

    return 0;
}